<template>
  <div id="app">
    <keep-alive exclude="Search,Login,Register,More,detailspage,Readcomics">
      <router-view v-if="isRouterAlive"/>
    </keep-alive>
  </div>
</template>

<script>
export default {
  name: 'App',
  provide () {
    return {
      reload: this.reload
    }
  },
  data () {
    return {
      isRouterAlive: true
    }
  },
  methods: {
    reload () {
      this.isRouterAlive = false
      this.$nextTick(function () {
        this.isRouterAlive = true
      })
    }
  }
}
</script>
